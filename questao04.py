
# Desvendando o Mistério dos Interruptores:

# 1ª Ida:

# Ligue o interruptor 1 e deixe-o ligado por 10 minutos.
# Desligue o interruptor 1 e ligue o interruptor 2.
# Vá para a sala das lâmpadas.
# Análise:

# Lâmpada 1:
# Se estiver acesa, o interruptor 1 a controla.
# Se estiver apagada e quente, o interruptor 1 a controla (deixou-a acesa por 10 minutos).
# Se estiver apagada e fria, o interruptor 3 a controla.
# Lâmpada 2:
# Se estiver acesa, o interruptor 2 a controla.
# Se estiver apagada, o interruptor 3 a controla.

########################################################################
# 2ª Ida (opcional):

# Se na primeira ida você não identificou a lâmpada do interruptor 3:

# Toque na Lâmpada 2.
# Se estiver quente, o interruptor 2 a controla.
# Se estiver fria, o interruptor 3 a controla.
# Com apenas duas idas, você terá a resposta completa!

